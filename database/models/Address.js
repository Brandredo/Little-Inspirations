const mongoose = require('mongoose');

let addressSchema = mongoose.Schema({

    street: {
        type: String,
        required: true
    },
    city: {
        type: String,
        required: true
    },
    state: {
        type: String,
        required: true
    },
    zipcode: {
        type: String,
        required: true
    }
});

module.exports = addressSchema;